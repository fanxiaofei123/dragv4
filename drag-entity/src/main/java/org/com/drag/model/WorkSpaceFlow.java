package org.com.drag.model;

import java.io.Serializable;
import java.util.List;

public class WorkSpaceFlow implements Serializable{
	
	/**
	
	 * @fieldName: serialVersionUID
	
	 * @fieldType: long
	
	 * @Description: TODO
	
	 */
	private static final long serialVersionUID = -9217411359890304350L;

	private String workSpaceName;
	private String workSpaceId;
	private String createContent;
	
	private List<WorkFlow> workFLowList;

	
	
	
	public String getCreateContent() {
		return createContent;
	}

	public void setCreateContent(String createContent) {
		this.createContent = createContent;
	}

	public String getWorkSpaceName() {
		return workSpaceName;
	}

	public void setWorkSpaceName(String workSpaceName) {
		this.workSpaceName = workSpaceName;
	}

	public List<WorkFlow> getWorkFLowList() {
		return workFLowList;
	}

	public void setWorkFLowList(List<WorkFlow> workFLowList) {
		this.workFLowList = workFLowList;
	}

	public String getWorkSpaceId() {
		return workSpaceId;
	}

	public void setWorkSpaceId(String workSpaceId) {
		this.workSpaceId = workSpaceId;
	}

	@Override
	public String toString() {
		return "WorkSpaceFlow [workSpaceName=" + workSpaceName + ", workSpaceId=" + workSpaceId + ", createContent="
				+ createContent + ", workFLowList=" + workFLowList + "]";
	}


	

}
