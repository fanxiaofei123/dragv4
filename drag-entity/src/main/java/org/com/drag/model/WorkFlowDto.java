package org.com.drag.model;

import java.util.ArrayList;
import java.util.List;

/**
 * Created by cdyoue on 2016/11/30.
 */
public class WorkFlowDto {
    private String serliza;
    private Integer flowId;
    List<ConfigDto> configs = new ArrayList<>();

    public String getSerliza() {
        return serliza;
    }

    public void setSerliza(String serliza) {
        this.serliza = serliza;
    }

    public List<ConfigDto> getConfigs() {
        return configs;
    }

    public void setConfigs(List<ConfigDto> configs) {
        this.configs = configs;
    }

    public Integer getFlowId() {
        return flowId;
    }

    public void setFlowId(Integer flowId) {
        this.flowId = flowId;
    }
}
