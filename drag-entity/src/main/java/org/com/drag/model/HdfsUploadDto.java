package org.com.drag.model;

import java.io.Serializable;

/**
 * Created by cdyoue on 2016/11/14.
 */
public class HdfsUploadDto implements Serializable{
    /**
	
	 * @fieldName: serialVersionUID
	
	 * @fieldType: long
	
	 * @Description: TODO
	
	 */
	private static final long serialVersionUID = 2852207025857495549L;
	private Integer userKey;
    private String path;
    private String usr;
    private String uploadPath;
    private Integer tipTimes;

    public HdfsUploadDto(String path, String usr, String uploadPath,Integer userKey,Integer tipTimes) {
        this.path = path;
        this.usr = usr;
        this.uploadPath = uploadPath;
        this.userKey = userKey;
        this.tipTimes = tipTimes;
    }


    public Integer getTipTimes() {
        return tipTimes;
    }

    public void setTipTimes(Integer tipTimes) {
        this.tipTimes = tipTimes;
    }

    public Integer getUserKey() {
        return userKey;
    }

    public void setUserKey(Integer userKey) {
        this.userKey = userKey;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getUsr() {
        return usr;
    }

    public void setUsr(String usr) {
        this.usr = usr;
    }

    public String getUploadPath() {
        return uploadPath;
    }

    public void setUploadPath(String uploadPath) {
        this.uploadPath = uploadPath;
    }
}
