package org.com.drag.model;

/**
 * 扩展{@link FacadeModel}，将其中的关联的外键对象都获取到了
 * */
public class ModelDetail extends FacadeModel {

	private static final long serialVersionUID = -2661707515582104194L;

	/**
     * 关联的算法工程师对象
     * */
    private User user;
    
    /**
     * 关联的FacadeUploadInfo对象
     * */
    private FacadeUploadInfo uploadInfo;
    
    private WorkFlow flow;
    
	public User getUser() {
		return user;
	}

	public void setUser(User user) {
		this.user = user;
	}

	public FacadeUploadInfo getUploadInfo() {
		return uploadInfo;
	}

	public void setUploadInfo(FacadeUploadInfo uploadInfo) {
		this.uploadInfo = uploadInfo;
	}

	public WorkFlow getFlow() {
		return flow;
	}

	public void setFlow(WorkFlow flow) {
		this.flow = flow;
	}
}
